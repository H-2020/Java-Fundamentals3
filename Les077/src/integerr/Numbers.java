package integerr;

public class Numbers {

    /* int -> Integer */
    private int integerPrimitive;
    private Integer integerAdvanced;

    private double doublePrimitive;
    private Double doubleAdvanced;

    public Numbers() {
    }

    public int getIntegerPrimitive() {
        return integerPrimitive;
    }

    public void setIntegerPrimitive(int integerPrimitive) {
        this.integerPrimitive = integerPrimitive;
    }

    public Integer getIntegerAdvanced() {
        return integerAdvanced;
    }

    public void setIntegerAdvanced(Integer integerAdvanced) {
        this.integerAdvanced = integerAdvanced;
    }

    public double getDoublePrimitive() {
        return doublePrimitive;
    }

    public void setDoublePrimitive(double doublePrimitive) {
        this.doublePrimitive = doublePrimitive;
    }

    public Double getDoubleAdvanced() {
        return doubleAdvanced;
    }

    public void setDoubleAdvanced(Double doubleAdvanced) {
        this.doubleAdvanced = doubleAdvanced;
    }
}
